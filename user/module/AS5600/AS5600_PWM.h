// ==================== 编码器方向反转标志 ====================
// 说明：由于机械安装方向不同，部分电机编码器方向与标准方向相反
// 反转公式：corrected = 4096 - original
//
// ┏━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┓
// ┃  宏定义          │ 电机索引 │ 物理位置 │ 是否反转 │        说明          ┃
// ┣━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┫
// ┃  MOTOR1_REVERSE  │   M1    │  右前    │    0     │ 正常方向（不反转）   ┃
// ┃  MOTOR3_REVERSE  │   M3    │  左前    │    1     │ 反转方向（需反转）   ┃
// ┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┛

#define MOTOR1_REVERSE 0  // M1(右前)编码器方向：0=正常（顺时针值增大）
#define MOTOR3_REVERSE 0  // M3(左前)编码器方向：1=反转（逆时针转时编码器值减小）

// PROCESS_VALUE宏：将原始编码器值转换为相对角度值
// 原理：将原始值相对于零点(zero)进行偏移，使zero位置对应1024（垂直位置）
// 公式：result = (raw - zero + 1024) mod 4096
// 其中1024对应90°（垂直位置），0对应0°，2048对应180°，3072对应270°
#define PROCESS_VALUE(raw, zero) (((raw) + 4096u - (((zero) + 3072u) & 0x0FFFu)) & 0x0FFFu)



