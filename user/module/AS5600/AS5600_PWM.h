// ==================== 【重要】电机中点校准参数（双电机版本）====================
//
// ┏━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┓
// ┃  电机中点定义表（零点校准用）                                              ┃
// ┣━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┫
// ┃  宏定义          │ ADC通道 │ STM32引脚 │ 物理电机 │ 对应Wings_motor索引 ┃
// ┣━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┫
// ┃  MOTOR1_MIDPOINT │ [1]     │   PA1     │  M1(右前)│        [0]          ┃
// ┃  MOTOR3_MIDPOINT │ [0]     │   PA0     │  M3(左前)│        [2]          ┃
// ┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┛
//
// ⚠️ 中点校准方法：
//   1. 将电机转到机械中位（翅膀竖直）
